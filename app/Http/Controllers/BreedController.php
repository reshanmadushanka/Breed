     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $breed = new Breed();
        $breed->adaptability = $request->adaptability;
        $breed->affection_level = $request->affection_level;
        $breed->child_friendly = $request->child_friendly;
        $breed->description = $request->description;
        $breed->energy_level = $request->energy_level;
        $breed->adaptability = $request->adaptability;
        $breed->name = $request->name;
        $breed->weight = $request->weight;
        $breed->stranger_friendly = $request->stranger_friendly;
        $breed->save();
        return redirect()->back();

    }

